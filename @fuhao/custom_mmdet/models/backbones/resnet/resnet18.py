class ResNet18_FPN_Cls(nn.Module):
    def __init__(self, cls_nums) -> None:
        super().__init__()

        self.cls_nums = cls_nums

        self.backbone = ResNet18()
        self.neck = FPN( 
            in_channels=[64, 128, 256, 512],
            out_channels=256,
            num_outs=5)
        
         # 全局池化
        self.neck_avg_Pooling = nn.AdaptiveAvgPool2d((1, 1))
        self.cls = nn.Sequential(
            nn.Linear(512, self.cls_nums)
        )
    
    def forward(self, x):
        outs = self.backbone(x)
        cls_input = outs[3]
        outs = self.neck(outs)
        
        # 分类 neck + head
        cls = self.neck_avg_Pooling(cls_input)
        cls = cls.view(cls.size(0), -1)
        cls = self.cls(cls)
        return outs, cls


